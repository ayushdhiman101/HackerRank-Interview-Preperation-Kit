static int jumpingOnClouds(int[] c) {
        int count=0,i=0;
        while(i<c.length-1){
            if(i+2==c.length||c[i+2]==1){
                i++;
                count++;
            }
            else
            {
                i+=2;
                count++;
            }
            
            
        }

return count;
    }
